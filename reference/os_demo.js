const os = require('os');

// Platform
console.log(os.platform());

// Arch
console.log(os.arch());

// CPU
console.log(os.cpus());

// Free Memory
console.log(os.freemem());

// Total Memory
console.log(os.totalmem());

// Home Directory
console.log(os.homedir());

// Uptime
console.log(os.uptime());